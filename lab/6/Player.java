
public class Player {
	private int goals;
	private String name;
	private String team;
	
Player(){
	
}

public int getGoals() {
	return goals;
}

public void setGoals(int goals) {
	this.goals = goals;
}

public String getName() {
	return name;
}

public void setName(String name) {
	this.name = name;
}

public String getTeam() {
	return team;
}

public void setTeam(String team) {
	this.team = team;
}

}
